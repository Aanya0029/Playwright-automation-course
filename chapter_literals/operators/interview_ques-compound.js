//compound operators

//| Operator | Meaning             | Example   |
//| -------- | ------------------- | --------- |
//| `+=`     | Add and assign      | `x += 5`  |
//| `-=`     | Subtract and assign | `x -= 5`  |
//| `*=`     | Multiply and assign | `x *= 5`  |
//| `/=`     | Divide and assign   | `x /= 5`  |
//| `%=`     | Modulus and assign  | `x %= 5`  |
//| `**=`    | Power and assign    | `x **= 2` |


let x =10;
x += 10;
console.log(x);

x -= 10;
console.log(x);

x *= 10;
console.log(x);     

x /= 10;
console.log(x); 

x %= 10;        
console.log(x);

x **= 2;        
console.log(x); 


