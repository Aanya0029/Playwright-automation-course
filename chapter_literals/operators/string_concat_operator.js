//String Concatenation Operator in JavaScript (+)

//In JavaScript, the + operator is used to join strings together, which is known as string concatenation. When you use the + operator with strings, it combines them into a single string.
let str1 = "Hello, ";
let str2 = "world!";
let result = str1 + str2;   // "Hello, world!"
console.log(result);

//You can also concatenate string literals directly:
let greeting = "Hi, " + "how are you?";
console.log(greeting);