//| Operator | Meaning             | Example  | Result        |
//| -------- | ------------------- | -------- | ------------- |
//| `+`      | Addition            | `5 + 2`  | `7`           |
//| `-`      | Subtraction         | `5 - 2`  | `3`           |
//| `*`      | Multiplication      | `5 * 2`  | `10`          |
//| `/`      | Division            | `10 / 2` | `5`           |
//| `%`      | Modulus (remainder) | `10 % 3` | `1`           |
//| `**`     | Exponent (power)    | `2 ** 3` | `8`           |
//| `++`     | Increment           | `x++`    | increase by 1 |
//| `--`     | Decrement           | `x--`    | decrease by 1 |

let a = 10;
let b = 3;

console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.333
console.log(a % b); // 1
console.log(a ** b); // 1000