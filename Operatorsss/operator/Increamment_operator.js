//Increment Operator (++) in JavaScript The increment operator is used to increase a value by 1.

let num = 10;

num++;

console.log(num); // Output: 11
//The increment operator can be used in two forms: prefix and postfix. The difference between them is the order of evaluation.

//Postfix Increment (num++)
let a = 5;
console.log(a++); // Output: 5 (returns the value before incrementing)
console.log(a);   // Output: 6 (the value is now incremented)


//Prefix Increment (++num)
let b = 5;
console.log(++b); // Output: 6 (increments the value before returning it)
console.log(b);   // Output: 6 (the value is already incremented)   





